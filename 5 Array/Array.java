public class Array {
    public static void main(String[] args) {
        // Declaring and initialising an array
        int[] numbers = {10, 20, 30, 40, 50};

        // Accessing elements
        for (int i = 0; i < numbers.length; i++) {
            System.out.println("Element at index " + i + ": " + numbers[i]);
        }
    }
}
