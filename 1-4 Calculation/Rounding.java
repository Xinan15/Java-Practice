public class Rounding {

    public static void main(String[] args) {

        // Rounding

        System.out.println(Math.floor(3.7)); // Rounds down

        System.out.println(Math.ceil(3.2)); // Rounds up

        System.out.println(Math.ceil(3.5)); // rounding off.

        // Modulus (Finding the Remainder)

        System.out.println(7 % 3); // 7 divided by 3 is 2 with a remainder of 1.

    }

}
