public class Divide {

    public static void main(String[] args) {
        
        // int
        System.out.println(4/2); // output: 2
        System.out.println(5/2); // output: 2

        // float
        System.out.println(4/2.0); // output: 2.0
        System.out.println(10/3.0); // output: 3.3333333333333335
    }
    
}
