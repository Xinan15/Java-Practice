public class Test1 {

    public static void main(String[] args) {
        int limit = 100;
        int num1 = 15;
        int num2 = 20;

        if (limit <= limit) {
            if (num1 == num2)
                System.out.println("lemon");
            System.out.println("lime");
        }
        System.out.println("grape");

        // output:
        // lime
        // grape

        // Indentation does not affect the execution or the logic of the program in Java
        // So curly braces {} are used to define the block of code that should be executed
        // Using curly braces even for single statements is considered good practice

    }

}
