public class Main {

    public static void main(String[] args) {
        

        // "Ternary operator" is a concise way to perform conditional assignments and decisions.
        
        // The ternary operator is a one-liner substitute for the if-then-else statement

        // condition ? expression1 : expression2

        // Here, condition is a Boolean expression. 
        // If the condition is true, expression1 is evaluated and becomes the result of the operation. 
        // If the condition is false, expression2 is evaluated and becomes the result.

        int a = 5, b = 10;
        
        int max = (a > b) ? a : b; // max will be 10
    
        System.out.println(max);

    }
    
}
